public class GoogleContactsException extends Exception {

	private static final long serialVersionUID = 5310599623350700159L;

	public GoogleContactsException() {
		super();
	}

	public GoogleContactsException(String arg0) {
		super(arg0);
	}

	public GoogleContactsException(Throwable arg0) {
		super(arg0);
	}

	public GoogleContactsException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
